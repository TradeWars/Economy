package org.tradewars.economy

import org.junit.jupiter.api.Test
import org.junit.jupiter.api.Assertions.assertTrue

/**
 * This class contains a simple example for writing a test with JUnit.
 *
 * @author Marcel
 * @since 07.07.2018
 */
class ExampleTest {

    /**
     * This Test will run every time that you run the Gradle task `test`.
     */
    @Test
    fun testExample() {
        assertTrue("Hello JUnit".isNotEmpty(), "If this ever fails, something is clearly wrong.")
    }
}